import type { Method, NamedType, OpensdkSpecJson, Resource } from '@xyd-js/opensdk-core';
import type { Emitter, EmitterContext, GeneratedFile } from '@xyd-js/opensdk-framework';
import { generate } from '@xyd-js/opensdk-framework';

import { renderClientFile } from './client';
import { renderModelsFile } from './model';
import { pascalCase, rubyGemName, screamingSnakeCase, snakeCase } from './naming';
import { renderEntrypoint, renderGemspec } from './project';
import { renderTransportFile } from './runtime';
import { generateRubyTypeReference, renderServiceFile, type RubyCtx } from './service';
import { generateRubyUsage, resourceTestRb, testHelperRb } from './tests-rb';
import type { OpensdkRubyOptions } from './types';

interface ResolvedRubyOptions {
  pkg: string;
  moduleName: string;
  baseURL: string;
  envVar: string;
}

function resolveOptions(spec: OpensdkSpecJson, ctx: EmitterContext): ResolvedRubyOptions {
  const options = ctx.emitterOptions as OpensdkRubyOptions;
  const pkg = options.packageName ?? rubyGemName(spec.info.title);
  return {
    pkg,
    moduleName: options.moduleName ?? pascalCase(spec.info.title),
    baseURL: options.baseURL ?? spec.servers?.[0] ?? '',
    envVar: spec.security?.find((s) => s.envVar)?.envVar ?? `${screamingSnakeCase(pkg)}_API_KEY`,
  };
}

function rubyCtx(spec: OpensdkSpecJson, ctx: EmitterContext): RubyCtx {
  const { pkg, moduleName } = resolveOptions(spec, ctx);
  return { moduleName, pkg, types: ctx.types as Map<string, NamedType> };
}

/**
 * The Ruby emitter plugin: a stdlib-only (net/http + json + uri), openai-ruby
 * shaped SDK — `module <Module>; class Client`, one resource class per resource
 * (nested, path-qualified), Struct/enum models and a vendored transport. Pure
 * capability methods over the IR; the framework orchestrator assembles the map.
 */
export const rubyEmitter: Emitter = {
  language: 'ruby',

  // The machine-ownership marker: the orchestrator prepends it (as `# ...`) to
  // every generated .rb file. The `.gemspec` is left untouched (unknown
  // extension), so its `frozen_string_literal` line-1 magic comment survives.
  fileHeader(): string {
    return 'Code generated by opensdk. DO NOT EDIT.';
  },

  generateProject(spec: OpensdkSpecJson, ctx: EmitterContext): GeneratedFile[] {
    const { pkg } = resolveOptions(spec, ctx);
    return [
      // User-owned scaffold: never clobber a hand-edited gemspec on regen.
      { path: `${pkg}.gemspec`, content: renderGemspec(spec, pkg), writeMode: 'skipIfExists' },
      { path: `lib/${pkg}.rb`, content: renderEntrypoint(spec, pkg) },
    ];
  },

  generateClient(spec: OpensdkSpecJson, ctx: EmitterContext): GeneratedFile[] {
    const { pkg, envVar } = resolveOptions(spec, ctx);
    return [{ path: `lib/${pkg}/client.rb`, content: renderClientFile(spec, rubyCtx(spec, ctx), envVar) }];
  },

  // models.rb is always emitted (even for an empty symbol table): the entry
  // point requires it unconditionally.
  generateTypes(_types: NamedType[], ctx: EmitterContext): GeneratedFile[] {
    const { pkg, moduleName } = resolveOptions(ctx.spec, ctx);
    return [{ path: `lib/${pkg}/models.rb`, content: renderModelsFile(ctx.spec, moduleName) }];
  },

  generateResources(resources: Resource[], ctx: EmitterContext): GeneratedFile[] {
    const rctx = rubyCtx(ctx.spec, ctx);
    return resources.map((resource) => renderServiceFile(resource, rctx));
  },

  generateRuntime(spec: OpensdkSpecJson, ctx: EmitterContext): GeneratedFile[] {
    const { pkg, moduleName, baseURL } = resolveOptions(spec, ctx);
    return [{ path: `lib/${pkg}/transport.rb`, content: renderTransportFile(spec, moduleName, pkg, baseURL) }];
  },

  // The SDK's own minitest suite (openai-ruby's test/**): one
  // test/test_<resource>.rb per top-level resource plus the shared test_helper.
  // Off via emitterOptions.tests === false; default ON. Example values come from
  // the shared planner so the Go/Python/Ruby suites exercise identical shapes.
  generateTests(spec: OpensdkSpecJson, ctx: EmitterContext): GeneratedFile[] {
    if ((ctx.emitterOptions as OpensdkRubyOptions).tests === false) return [];
    const { pkg, moduleName } = resolveOptions(spec, ctx);
    const resources = spec.resources || [];
    if (resources.length === 0) return [];
    const types = ctx.types as Map<string, NamedType>;
    const files: GeneratedFile[] = [{ path: 'test/test_helper.rb', content: testHelperRb(moduleName, pkg) }];
    for (const r of resources) {
      files.push({ path: `test/test_${snakeCase(r.name)}.rb`, content: resourceTestRb(r, moduleName, types) });
    }
    return files;
  },

  // A single per-operation doc USAGE SNIPPET: requires the gem, constructs the
  // client and makes one required-only call (à la Fern/Speakeasy/Stainless).
  // `chain` is the resource-name path (root→owner) the method hangs off.
  generateUsage(method: Method, chain: string[], ctx: EmitterContext): string {
    return generateRubyUsage(method, chain, ctx.spec, ctx.emitterOptions as OpensdkRubyOptions);
  },

  // The per-operation SDK type reference (signature + request/response types).
  generateTypeReference(method: Method, chain: string[], ctx: EmitterContext) {
    return generateRubyTypeReference(method, chain, rubyCtx(ctx.spec, ctx));
  },
};

/**
 * Generate a buildable, openai-ruby-shaped Ruby SDK from an OpenSDK IR.
 * Back-compat wrapper: drives the `rubyEmitter` plugin through the framework
 * orchestrator. Pure: returns a virtual file map `{ relativePath: contents }`.
 */
export function opensdkRuby(spec: OpensdkSpecJson, options: OpensdkRubyOptions = {}): Record<string, string> {
  return generate(spec, rubyEmitter, options as Record<string, unknown>);
}
