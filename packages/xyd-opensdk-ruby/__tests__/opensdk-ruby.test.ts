import { walkMethods } from '@xyd-js/opensdk-core';
import { describe, expect, it } from 'vitest';

import { opensdkRuby, rubyEmitter } from '../index';
import { RUBY_SMOKE, readIR, rubyCompileSmoke, testFixture } from './utils';

const fixtures = [
  { name: '1.basic', description: 'client, resource classes, typed methods, Struct/enum models, stdlib transport' },
  {
    name: '2.wire',
    description:
      'wire robustness: multipart upload (StringIO file part), form body, header params, wireName + explode query serialization, sdk-behavior transport (retry policy, default timeout, UA, error kinds + request_id, idempotency injection)',
  },
  {
    name: '3.unions',
    description:
      'type-system consumption: discriminated-union decode (mapping + raw fallback), mapping-less union passthrough, cursor/offset pagination containers',
  },
  {
    name: '9.x-open-sdk',
    description:
      'x-open-sdk naming overrides: method/resource names from the IR action + resource tree (catalog.browse, system.health.check, things.fetch)',
  },
];

describe('opensdk-ruby', () => {
  for (const f of fixtures) {
    it(f.description, () => {
      testFixture(f.name);
    });
  }

  if (RUBY_SMOKE) {
    for (const f of fixtures) {
      it(`ruby -c: ${f.name}`, () => {
        rubyCompileSmoke(f.name);
      });
    }
  }
});

// The Ruby SDK mirrors the Go/Python emitters: same IR in, a language-native
// SDK out, driven through the same framework orchestrator.
describe('opensdk-ruby shape', () => {
  it('prepends the ownership header to every generated .rb file (and never to the gemspec)', () => {
    const files = opensdkRuby(readIR('1.basic'));
    for (const [rel, content] of Object.entries(files)) {
      if (rel.endsWith('.rb')) {
        expect(content.startsWith('# Code generated by opensdk. DO NOT EDIT.\n\n'), rel).toBe(true);
      }
    }
    expect(files['petstore.gemspec']).not.toContain('DO NOT EDIT');
    expect(files['petstore.gemspec']).toContain('# frozen_string_literal: true');
  });

  it('emits the openai-ruby-shaped module/class layout', () => {
    const files = opensdkRuby(readIR('1.basic'));
    expect(files['lib/petstore/client.rb']).toContain('module Petstore');
    expect(files['lib/petstore/client.rb']).toContain('class Client');
    expect(files['lib/petstore/client.rb']).toContain('attr_reader :pets');
    expect(files['lib/petstore/client.rb']).toContain('api_key = ENV["PETSTORE_API_KEY"] if api_key.nil?');

    const pets = files['lib/petstore/resources/pets.rb'];
    expect(pets).toContain('module Resources');
    expect(pets).toContain('class Pets');
    // path params positional, query/body as kwargs
    expect(pets).toContain('def list(after: nil, limit: nil)');
    expect(pets).toContain('def create(name:, status: nil)');
    expect(pets).toContain('def retrieve(pet_id)');
    // required string path param is guarded
    expect(pets).toContain('raise ArgumentError, "Expected a non-empty value for `pet_id`"');
    // cursor list wraps the response in a Page
    expect(pets).toContain('Petstore::Page.new(data: (response || {})[:data], has_more: (response || {})[:has_more])');
    // binary download returns raw bytes
    expect(pets).toContain('def download_photo(pet_id)');
    expect(pets).toContain('raw: true');
  });

  it('emits Struct models and an enum module', () => {
    const models = opensdkRuby(readIR('1.basic'))['lib/petstore/models.rb'];
    expect(models).toContain('module Models');
    expect(models).toContain('Pet = Struct.new(:id, :name, :status, :tags, keyword_init: true)');
    expect(models).toContain('module PetStatus');
    expect(models).toContain('AVAILABLE = "available"');
  });

  it('generateUsage: a self-contained per-operation doc snippet (client init + one required-only call)', () => {
    const spec = readIR('9.x-open-sdk');
    const types = new Map((spec.types || []).map((t: { name: string }) => [t.name, t]));
    const flat = walkMethods(spec).find(({ method }) => method.action === 'browse');
    expect(flat).toBeTruthy();

    const snippet = rubyEmitter.generateUsage!(flat!.method, flat!.path, {
      spec,
      types,
      emitterOptions: {},
    });

    // requires the gem, constructs the client from the env-var API key, and
    // calls the resource chain in ruby (snake_case) casing.
    expect(snippet).toContain('require "extensions_demo"');
    expect(snippet).toContain('client = ExtensionsDemo::Client.new(api_key: ENV["EXTENSIONS_DEMO_API_KEY"])');
    // catalog.browse returns an array → result is captured and printed.
    expect(snippet).toContain('result = client.catalog.browse');
    expect(snippet).toContain('pp result');
  });

  it('bakes the sdk-behavior policy into the stdlib transport', () => {
    const transport = opensdkRuby(readIR('1.basic'))['lib/petstore/transport.rb'];
    expect(transport).toContain('DEFAULT_BASE_URL = "https://api.petstore.io/v1"');
    expect(transport).toContain('USER_AGENT = "petstore-ruby/1.2.0"');
    expect(transport).toContain('MAX_RETRIES = 2');
    expect(transport).toContain('RETRYABLE_STATUS_CODES = [408, 429, 500, 502, 503, 504].freeze');
    expect(transport).toContain('REQUEST_ID_HEADER = "X-Request-ID"');
    expect(transport).toContain('class NotFoundError < APIError');
    expect(transport).toContain('404 => NotFoundError,');
    expect(transport).toContain('request_headers["Authorization"] = "Bearer #{@api_key}"');
    // stdlib-only: no third-party requires
    expect(transport).toContain('require "net/http"');
    expect(transport).not.toContain('require "faraday"');
  });
});
