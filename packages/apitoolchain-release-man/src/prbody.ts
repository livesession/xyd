import { groupChanges } from "./group";
import type { BumpType, ReleaseChange } from "./types";

function list(title: string, changes: ReleaseChange[]): string[] {
  if (changes.length === 0) return [];
  const lines = [`### ${title}`];
  for (const c of changes) lines.push(`- \`${c.path}\` — ${c.detail}`);
  lines.push("");
  return lines;
}

/**
 * Render the rolling release PR body (Speakeasy-style): a version-bump badge, a
 * breaking-change count table, a collapsible grouped change list, and a footer
 * that tells the user how to override the version. Deterministic.
 */
export function renderPrBody(i: {
  apiName: string;
  language: string;
  fromVersion: string;
  toVersion: string;
  bump: BumpType;
  headSpecVersion: string;
  baseSpecVersion: string;
  changes: readonly ReleaseChange[];
  overridden?: boolean;
}): string {
  const g = groupChanges(i.changes);
  const total = i.changes.length;
  const src = i.overridden ? "✍️ manual override" : "🤖 auto (from spec diff)";
  const base = i.baseSpecVersion || "∅";
  const out: string[] = [
    `# SDK release: ${i.apiName} (${i.language})`,
    "",
    "## Versioning",
    `**Bump:** \`${i.bump}\` — v${i.fromVersion || "0.0.0"} → **v${i.toVersion}**  · ${src}`,
    `**Spec:** ${base} → ${i.headSpecVersion || "current"}`,
    "",
    `## Changes (${total} total, ${g.breaking.length} breaking)`,
    "",
    "| Impact | Count |",
    "| --- | --- |",
    `| ⚠ Breaking | ${g.breaking.length} |`,
    `| ✨ Additive | ${g.additive.length} |`,
    `| 🔧 Other | ${g.other.length} |`,
    "",
  ];
  if (total === 0) {
    out.push("_No API changes detected._", "");
  } else {
    out.push(
      "<details><summary>Change detail</summary>",
      "",
      ...list("⚠ Breaking", g.breaking),
      ...list("✨ Additive", g.additive),
      ...list("🔧 Other", g.other),
      "</details>",
      "",
    );
  }
  out.push(
    "---",
    "🤖 Generated by apitoolchain-release-man. To override the version, edit this PR's title (e.g. `release: … v2.0.0`).",
  );
  return `${out.join("\n").trimEnd()}\n`;
}
