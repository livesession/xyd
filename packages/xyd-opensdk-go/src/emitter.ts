import { type NamedType, type OpensdkSpecJson, type Resource, sdkBehavior } from '@xyd-js/opensdk-core';
import type { Emitter, EmitterContext, GeneratedFile } from '@xyd-js/opensdk-framework';

import { renderClientFile } from './client';
import { generateGoTests, generateGoTypeReference, generateGoUsage } from './example-go';
import { renderTypesFile } from './model';
import { goPackageName } from './naming';
import { runtimeFiles } from './runtime';
import { renderServiceFile } from './service';
import type { GoCtx } from './service';
import type { OpensdkGoOptions } from './types';

interface ResolvedGoOptions {
  pkg: string;
  modulePath: string;
  goVersion: string;
  baseURL: string;
}

function resolveOptions(spec: OpensdkSpecJson, ctx: EmitterContext): ResolvedGoOptions {
  const options = ctx.emitterOptions as OpensdkGoOptions;
  const pkg = options.packageName ?? goPackageName(spec.info.title);
  return {
    pkg,
    modulePath: options.modulePath ?? `github.com/example/${pkg}`,
    goVersion: options.goVersion ?? '1.22',
    baseURL: options.baseURL ?? spec.servers?.[0] ?? '',
  };
}

function goCtx(spec: OpensdkSpecJson, ctx: EmitterContext): GoCtx {
  const { pkg, modulePath } = resolveOptions(spec, ctx);
  const { baseUrlEnv } = ctx.emitterOptions as OpensdkGoOptions;
  return { modulePath, pkg, types: ctx.types as Map<string, NamedType>, behavior: sdkBehavior(spec), baseUrlEnv };
}

/**
 * The Go emitter plugin: a Stainless-shaped, openai-go-style buildable SDK.
 * Pure capability methods over the IR — the framework orchestrator assembles
 * the file map.
 */
export const goEmitter: Emitter = {
  language: 'go',

  // The machine-ownership marker: the orchestrator prepends it (as `// ...`)
  // to every .go file — the idiomatic marker Go tooling and the regen
  // lifecycle (manifest + guarded prune) key off.
  fileHeader(): string {
    return 'Code generated by opensdk. DO NOT EDIT.';
  },

  generateProject(spec: OpensdkSpecJson, ctx: EmitterContext): GeneratedFile[] {
    const { modulePath, goVersion } = resolveOptions(spec, ctx);
    return [{ path: 'go.mod', content: `module ${modulePath}\n\ngo ${goVersion}\n` }];
  },

  generateClient(spec: OpensdkSpecJson, ctx: EmitterContext): GeneratedFile[] {
    return [{ path: 'client.go', content: renderClientFile(spec, goCtx(spec, ctx)) }];
  },

  generateTypes(types: NamedType[], ctx: EmitterContext): GeneratedFile[] {
    if (types.length === 0) return [];
    const { pkg } = resolveOptions(ctx.spec, ctx);
    return [{ path: 'types.go', content: renderTypesFile(ctx.spec, pkg) }];
  },

  generateResources(resources: Resource[], ctx: EmitterContext): GeneratedFile[] {
    const gctx = goCtx(ctx.spec, ctx);
    return resources.map((resource) => {
      const file = renderServiceFile(resource, gctx);
      return { path: file.path, content: file.content };
    });
  },

  generateRuntime(spec: OpensdkSpecJson, ctx: EmitterContext): GeneratedFile[] {
    const { modulePath, baseURL, pkg } = resolveOptions(spec, ctx);
    return Object.entries(runtimeFiles(spec, modulePath, baseURL, pkg)).map(([path, content]) => ({ path, content }));
  },

  // The SDK's OWN openai-go-shaped test suite. Default ON; opt out with
  // emitterOptions.tests === false.
  generateTests(spec: OpensdkSpecJson, ctx: EmitterContext): GeneratedFile[] {
    if ((ctx.emitterOptions as OpensdkGoOptions).tests === false) return [];
    return generateGoTests(spec, goCtx(spec, ctx));
  },

  // A single per-operation usage snippet for docs (client init + one call).
  generateUsage(method, chain, ctx: EmitterContext): string {
    return generateGoUsage(method, chain, goCtx(ctx.spec, ctx));
  },

  // The per-operation SDK type reference (signature + request/response types).
  generateTypeReference(method, chain, ctx: EmitterContext) {
    return generateGoTypeReference(method, chain, goCtx(ctx.spec, ctx));
  },
};
