// ---- SdkBehavior interpolation (sdk.* policies -> generated runtime) -------
// The goldens above prove the canonical DEFAULTS; this block proves the
// override channel: a spec.sdk block flows into the generated constants.
describe('opensdk-go sdk-behavior interpolation', () => {
  const readIR = (name: string) =>
    JSON.parse(fs.readFileSync(path.join(__dirname, '../__fixtures__', name, 'input.json'), 'utf8'));

  it('every generated file carries the engine ownership header', () => {
    const files = opensdkGo(readIR('1.basic'));
    for (const [rel, content] of Object.entries(files)) {
      if (!rel.endsWith('.go')) continue;
      expect(content.startsWith('// Code generated by opensdk. DO NOT EDIT.\n\n'), rel).toBe(true);
    }
  });

  it('canonical defaults land in the runtime (retry, timeout, UA sniff, error kinds)', () => {
    const files = opensdkGo(readIR('2.wire'));
    const config = files['internal/requestconfig/config.go'];
    expect(config).toMatch(/defaultMaxRetries\s+= 2\n/);
    expect(config).toMatch(/honorRetryAfterHeader\s+= true\n/);
    expect(config).toContain('408: true,');
    expect(config).toContain('const defaultRequestTimeout = 60000 * time.Millisecond');
    expect(config).toContain('const requestIDHeader = "X-Request-ID"');
    expect(config).toContain('{"CLAUDE_CODE", "claude-code"},');

    const errors = files['internal/requestconfig/errors.go'];
    expect(errors).toContain('type NotFoundError struct{ *APIError }');
    expect(errors).toContain('type RateLimitedError struct{ *APIError }');
    expect(errors).toContain('type InternalError struct{ *APIError }');
    expect(errors).toContain('func errorDocSuffix(_ *APIError) string { return "" }');

    // The flagged POST injects one generated key per logical call.
    expect(files['tokens.go']).toContain('cfg.Header.Set("Idempotency-Key", requestconfig.NewIdempotencyKey())');
    expect(files['internal/requestconfig/requestconfig.go']).toContain('func NewIdempotencyKey() string');
    // The unflagged multipart POST does not.
    expect(files['files.go']).not.toContain('NewIdempotencyKey');
  });

  it('spec.sdk overrides flow into the generated constants', () => {
    const spec = {
      ...readIR('2.wire'),
      sdk: {
        retry: {
          maxRetries: 5,
          retryableStatusCodes: [429],
          backoff: { initialDelayMs: 100, maxDelayMs: 2000, multiplier: 3, jitter: 0 },
        },
        timeout: { defaultTimeoutMs: 1234, timeoutEnvVar: 'WIRE_TIMEOUT_MS' },
        errors: { statusCodeMap: { '404': 'Missing' }, errorDocUrlTemplate: 'https://errs.test/{kind}' },
        userAgent: { sdkIdentifierTemplate: 'x-{package}-{language}/{version}', aiAgentEnvVars: {} },
        telemetry: { requestIdHeader: 'X-Req' },
        idempotency: { headerName: 'X-Idem' },
        pagination: { autoPageDelayMs: 350 },
      },
    };
    const files = opensdkGo(spec);
    const config = files['internal/requestconfig/config.go'];
    expect(config).toMatch(/defaultMaxRetries\s+= 5\n/);
    expect(config).toContain('map[int]bool{\n\t429: true,\n}');
    expect(config).toMatch(/backoffInitialDelay\s+= 100 \* time\.Millisecond\n/);
    expect(config).toMatch(/backoffMultiplier\s+= 3\.0\n/);
    expect(config).toMatch(/backoffJitter\s+= 0\.0\n/);
    expect(config).toContain('os.Getenv("WIRE_TIMEOUT_MS")');
    expect(config).toContain('return 1234 * time.Millisecond');
    expect(config).toContain('const requestIDHeader = "X-Req"');
    // Template substitution ({package}/{language}/{version}); the agent table
    // deep-merges (objects merge, arrays replace), so the defaults survive an
    // empty-object override and the UA stays init-assembled.
    expect(config).toContain('ua := "x-wirekitchen-go/0.3.0"');
    expect(config).toContain('{"CLAUDE_CODE", "claude-code"},');

    const errors = files['internal/requestconfig/errors.go'];
    expect(errors).toContain('type MissingError struct{ *APIError }');
    expect(errors).not.toContain('NotFoundError');
    expect(errors).toContain('strings.ReplaceAll("https://errs.test/{kind}", "{kind}", e.Kind)');

    // Idempotency header name is policy-driven.
    expect(files['tokens.go']).toContain('cfg.Header.Set("X-Idem", requestconfig.NewIdempotencyKey())');
  });

  it('runtime-version suffix and pagination delay are policy-driven', () => {
    const unions = {
      ...readIR('3.unions'),
      sdk: {
        userAgent: { includeRuntimeVersion: true },
        pagination: { autoPageDelayMs: 350 },
      },
    };
    const files = opensdkGo(unions);
    const config = files['internal/requestconfig/config.go'];
    expect(config).toContain('ua += " " + runtime.Version()');
    expect(files['packages/pagination/pagination.go']).toContain('const autoPageDelay = 350 * time.Millisecond');

    // Retry disabled => no idempotency injection even for flagged methods.
    const noRetry = { ...readIR('2.wire'), sdk: { retry: { maxRetries: 0 } } };
    const noRetryFiles = opensdkGo(noRetry);
    expect(noRetryFiles['tokens.go']).not.toContain('NewIdempotencyKey');
    expect(noRetryFiles['internal/requestconfig/requestconfig.go']).not.toContain('NewIdempotencyKey');
  });
});

describe('opensdk-go generateUsage', () => {
  it('emits a self-contained client-init + one-call usage snippet', () => {
    const ir = JSON.parse(
      fs.readFileSync(path.join(__dirname, '../__fixtures__/9.x-open-sdk/input.json'), 'utf8'),
    );
    const types = new Map<string, NamedType>((ir.types || []).map((t: NamedType) => [t.name, t]));
    const ctx: EmitterContext = { spec: ir, types, emitterOptions: {} };

    const browse = walkMethods(ir).find((f) => f.method.action === 'browse');
    expect(browse).toBeTruthy();

    const code = goEmitter.generateUsage!(browse!.method, browse!.path, ctx);
    expect(code).toContain('package main');
    expect(code).toContain('.NewClient(');
    expect(code).toContain('client.Catalog.Browse(');
    expect(code).toContain('context.TODO()');
  });
});
