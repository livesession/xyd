package cmd

import (
	"context"
	"encoding/json"
	"net/url"

	"example.com/openai/internal/runtime"
	"github.com/urfave/cli/v3"
)

func NewThreadsCommand() *cli.Command {
	return &cli.Command{
		Name: "threads",
		Commands: []*cli.Command{
			&cli.Command{
				Name: "messages",
				Commands: []*cli.Command{
					&cli.Command{
						Name: "create",
						Usage: "Create a message.",
						Flags: []cli.Flag{
							&cli.StringFlag{
								Name: "role",
								Usage: "The role of the entity that is creating the message. Allowed values include:\n- `user`: Indicates the message is sent by an actual user and should be used in most cases to represent user-generated messages.\n- `assistant`: Indicates the message is generated by the assistant. Use this value to insert messages from the assistant into the conversation.\n",
								Required: true,
							},
							&cli.StringFlag{
								Name: "content",
								Required: true,
							},
							&cli.StringFlag{
								Name: "attachments",
							},
							&cli.StringFlag{
								Name: "metadata",
							},
						},
						Action: handleThreadsMessagesCreate,
					},
				},
			},
		},
	}
}

func handleThreadsMessagesCreate(ctx context.Context, cmd *cli.Command) error {
	threadID := cmd.Args().Get(0)
	path := "/threads/" + url.PathEscape(threadID) + "/messages"
	body := map[string]any{}
	if cmd.IsSet("role") {
		body["role"] = cmd.String("role")
	}
	if cmd.IsSet("content") {
		raw := cmd.String("content")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["content"] = v
	}
	if cmd.IsSet("attachments") {
		raw := cmd.String("attachments")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["attachments"] = v
	}
	if cmd.IsSet("metadata") {
		raw := cmd.String("metadata")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["metadata"] = v
	}
	bodyBytes, err := json.Marshal(body)
	if err != nil {
		return err
	}
	req := runtime.Request{
		Method: "POST",
		Path: path,
		Body: bodyBytes,
	}
	return runtime.Do(ctx, req)
}
