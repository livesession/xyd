package cmd

import (
	"context"
	"encoding/json"

	"example.com/openai/internal/runtime"
	"github.com/urfave/cli/v3"
)

func NewRealtimeCommand() *cli.Command {
	return &cli.Command{
		Name: "realtime",
		Commands: []*cli.Command{
			&cli.Command{
				Name: "calls",
				Commands: []*cli.Command{
					&cli.Command{
						Name: "create",
						Usage: "Create a new Realtime API call over WebRTC and receive the SDP answer needed\nto complete the peer connection.",
						Flags: []cli.Flag{
							&cli.StringFlag{
								Name: "sdp",
								Usage: "WebRTC Session Description Protocol (SDP) offer generated by the caller.",
								Required: true,
							},
							&cli.StringFlag{
								Name: "session",
								Usage: "Optional session configuration to apply before the realtime session is\ncreated. Use the same parameters you would send in a [`create client secret`](/docs/api-reference/realtime-sessions/create-realtime-client-secret)\nrequest.",
							},
						},
						Action: handleRealtimeCallsCreate,
					},
				},
			},
		},
	}
}

func handleRealtimeCallsCreate(ctx context.Context, cmd *cli.Command) error {
	path := "/realtime/calls"
	body := map[string]any{}
	if cmd.IsSet("sdp") {
		body["sdp"] = cmd.String("sdp")
	}
	if cmd.IsSet("session") {
		raw := cmd.String("session")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["session"] = v
	}
	bodyBytes, err := json.Marshal(body)
	if err != nil {
		return err
	}
	req := runtime.Request{
		Method: "POST",
		Path: path,
		Body: bodyBytes,
	}
	return runtime.Do(ctx, req)
}
