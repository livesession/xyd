package cmd

import (
	"context"
	"encoding/json"

	"example.com/openai/internal/runtime"
	"github.com/urfave/cli/v3"
)

func NewCompletionsCommand() *cli.Command {
	return &cli.Command{
		Name: "completions",
		Commands: []*cli.Command{
			&cli.Command{
				Name: "create",
				Usage: "Creates a completion for the provided prompt and parameters.\n\nReturns a completion object, or a sequence of completion objects if the request is streamed.\n",
				Flags: []cli.Flag{
					&cli.StringFlag{
						Name: "model",
						Usage: "ID of the model to use. You can use the [List models](/docs/api-reference/models/list) API to see all of your available models, or see our [Model overview](/docs/models) for descriptions of them.\n",
						Required: true,
					},
					&cli.StringFlag{
						Name: "prompt",
						Usage: "The prompt(s) to generate completions for, encoded as a string, array of strings, array of tokens, or array of token arrays.\n\nNote that <|endoftext|> is the document separator that the model sees during training, so if a prompt is not specified the model will generate as if from the beginning of a new document.\n",
						Required: true,
					},
					&cli.IntFlag{
						Name: "best-of",
						Usage: "Generates `best_of` completions server-side and returns the \"best\" (the one with the highest log probability per token). Results cannot be streamed.\n\nWhen used with `n`, `best_of` controls the number of candidate completions and `n` specifies how many to return – `best_of` must be greater than `n`.\n\n**Note:** Because this parameter generates many completions, it can quickly consume your token quota. Use carefully and ensure that you have reasonable settings for `max_tokens` and `stop`.\n",
					},
					&cli.BoolFlag{
						Name: "echo",
						Usage: "Echo back the prompt in addition to the completion\n",
					},
					&cli.FloatFlag{
						Name: "frequency-penalty",
						Usage: "Number between -2.0 and 2.0. Positive values penalize new tokens based on their existing frequency in the text so far, decreasing the model's likelihood to repeat the same line verbatim.\n\n[See more information about frequency and presence penalties.](/docs/guides/text-generation)\n",
					},
					&cli.StringFlag{
						Name: "logit-bias",
						Usage: "Modify the likelihood of specified tokens appearing in the completion.\n\nAccepts a JSON object that maps tokens (specified by their token ID in the GPT tokenizer) to an associated bias value from -100 to 100. You can use this [tokenizer tool](/tokenizer?view=bpe) to convert text to token IDs. Mathematically, the bias is added to the logits generated by the model prior to sampling. The exact effect will vary per model, but values between -1 and 1 should decrease or increase likelihood of selection; values like -100 or 100 should result in a ban or exclusive selection of the relevant token.\n\nAs an example, you can pass `{\"50256\": -100}` to prevent the <|endoftext|> token from being generated.\n",
					},
					&cli.IntFlag{
						Name: "logprobs",
						Usage: "Include the log probabilities on the `logprobs` most likely output tokens, as well the chosen tokens. For example, if `logprobs` is 5, the API will return a list of the 5 most likely tokens. The API will always return the `logprob` of the sampled token, so there may be up to `logprobs+1` elements in the response.\n\nThe maximum value for `logprobs` is 5.\n",
					},
					&cli.IntFlag{
						Name: "max-tokens",
						Usage: "The maximum number of [tokens](/tokenizer) that can be generated in the completion.\n\nThe token count of your prompt plus `max_tokens` cannot exceed the model's context length. [Example Python code](https://cookbook.openai.com/examples/how_to_count_tokens_with_tiktoken) for counting tokens.\n",
					},
					&cli.IntFlag{
						Name: "n",
						Usage: "How many completions to generate for each prompt.\n\n**Note:** Because this parameter generates many completions, it can quickly consume your token quota. Use carefully and ensure that you have reasonable settings for `max_tokens` and `stop`.\n",
					},
					&cli.FloatFlag{
						Name: "presence-penalty",
						Usage: "Number between -2.0 and 2.0. Positive values penalize new tokens based on whether they appear in the text so far, increasing the model's likelihood to talk about new topics.\n\n[See more information about frequency and presence penalties.](/docs/guides/text-generation)\n",
					},
					&cli.IntFlag{
						Name: "seed",
						Usage: "If specified, our system will make a best effort to sample deterministically, such that repeated requests with the same `seed` and parameters should return the same result.\n\nDeterminism is not guaranteed, and you should refer to the `system_fingerprint` response parameter to monitor changes in the backend.\n",
					},
					&cli.StringFlag{
						Name: "stop",
						Usage: "Not supported with latest reasoning models `o3` and `o4-mini`.\n\nUp to 4 sequences where the API will stop generating further tokens. The\nreturned text will not contain the stop sequence.\n",
					},
					&cli.BoolFlag{
						Name: "stream",
						Usage: "Whether to stream back partial progress. If set, tokens will be sent as data-only [server-sent events](https://developer.mozilla.org/en-US/docs/Web/API/Server-sent_events/Using_server-sent_events#Event_stream_format) as they become available, with the stream terminated by a `data: [DONE]` message. [Example Python code](https://cookbook.openai.com/examples/how_to_stream_completions).\n",
					},
					&cli.StringFlag{
						Name: "stream-options",
					},
					&cli.StringFlag{
						Name: "suffix",
						Usage: "The suffix that comes after a completion of inserted text.\n\nThis parameter is only supported for `gpt-3.5-turbo-instruct`.\n",
					},
					&cli.FloatFlag{
						Name: "temperature",
						Usage: "What sampling temperature to use, between 0 and 2. Higher values like 0.8 will make the output more random, while lower values like 0.2 will make it more focused and deterministic.\n\nWe generally recommend altering this or `top_p` but not both.\n",
					},
					&cli.FloatFlag{
						Name: "top-p",
						Usage: "An alternative to sampling with temperature, called nucleus sampling, where the model considers the results of the tokens with top_p probability mass. So 0.1 means only the tokens comprising the top 10% probability mass are considered.\n\nWe generally recommend altering this or `temperature` but not both.\n",
					},
					&cli.StringFlag{
						Name: "user",
						Usage: "A unique identifier representing your end-user, which can help OpenAI to monitor and detect abuse. [Learn more](/docs/guides/safety-best-practices#end-user-ids).\n",
					},
				},
				Action: handleCompletionsCreate,
			},
		},
	}
}

func handleCompletionsCreate(ctx context.Context, cmd *cli.Command) error {
	path := "/completions"
	body := map[string]any{}
	if cmd.IsSet("model") {
		raw := cmd.String("model")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["model"] = v
	}
	if cmd.IsSet("prompt") {
		raw := cmd.String("prompt")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["prompt"] = v
	}
	if cmd.IsSet("best-of") {
		body["best_of"] = cmd.Int("best-of")
	}
	if cmd.IsSet("echo") {
		body["echo"] = cmd.Bool("echo")
	}
	if cmd.IsSet("frequency-penalty") {
		body["frequency_penalty"] = cmd.Float("frequency-penalty")
	}
	if cmd.IsSet("logit-bias") {
		raw := cmd.String("logit-bias")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["logit_bias"] = v
	}
	if cmd.IsSet("logprobs") {
		body["logprobs"] = cmd.Int("logprobs")
	}
	if cmd.IsSet("max-tokens") {
		body["max_tokens"] = cmd.Int("max-tokens")
	}
	if cmd.IsSet("n") {
		body["n"] = cmd.Int("n")
	}
	if cmd.IsSet("presence-penalty") {
		body["presence_penalty"] = cmd.Float("presence-penalty")
	}
	if cmd.IsSet("seed") {
		body["seed"] = cmd.Int("seed")
	}
	if cmd.IsSet("stop") {
		raw := cmd.String("stop")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["stop"] = v
	}
	if cmd.IsSet("stream") {
		body["stream"] = cmd.Bool("stream")
	}
	if cmd.IsSet("stream-options") {
		raw := cmd.String("stream-options")
		var v any
		if err := json.Unmarshal([]byte(raw), &v); err != nil {
			v = raw
		}
		body["stream_options"] = v
	}
	if cmd.IsSet("suffix") {
		body["suffix"] = cmd.String("suffix")
	}
	if cmd.IsSet("temperature") {
		body["temperature"] = cmd.Float("temperature")
	}
	if cmd.IsSet("top-p") {
		body["top_p"] = cmd.Float("top-p")
	}
	if cmd.IsSet("user") {
		body["user"] = cmd.String("user")
	}
	bodyBytes, err := json.Marshal(body)
	if err != nil {
		return err
	}
	req := runtime.Request{
		Method: "POST",
		Path: path,
		Body: bodyBytes,
	}
	return runtime.Do(ctx, req)
}
