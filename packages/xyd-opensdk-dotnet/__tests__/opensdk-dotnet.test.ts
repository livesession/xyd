import fs from 'node:fs';
import path from 'node:path';

import { describe, expect, it } from 'vitest';

import { opensdkDotnet } from '../index';
import { DOTNET_SMOKE, dotnetBuildSmoke, testFixture } from './utils';

const fixtures: { name: string; description: string }[] = [
  {
    name: '1.basic',
    description:
      'client with resource-service properties, typed async methods, model classes + string enum, cursor page return, dependency-free transport + generated tests',
  },
  {
    name: '2.wire',
    description:
      'wire robustness: multipart upload (byte[] part), form body, header params, wireName + explode/deepObject query serialization, sdk-behavior runtime (retry policy, default timeout, UA, error kinds + RequestId, idempotency injection)',
  },
  {
    name: '3.unions',
    description:
      'type-system consumption: discriminated-union decode (mapping + raw fallback), const auto-fill on request, cursor/offset page returns',
  },
];

describe('opensdk-dotnet', () => {
  for (const f of fixtures) {
    it(f.description, () => {
      testFixture(f.name);
    });
  }

  if (DOTNET_SMOKE) {
    for (const f of fixtures) {
      it(`dotnet build: ${f.name}`, () => {
        dotnetBuildSmoke(f.name);
      });
    }
  }
});

// Structural guards that don't depend on the golden bytes — cheap invariants an
// integrator can rely on while iterating on the emitter.
describe('opensdk-dotnet shape', () => {
  const readIR = (name: string) =>
    JSON.parse(fs.readFileSync(path.join(__dirname, '../__fixtures__', name, 'input.json'), 'utf8'));

  it('every generated file carries the ownership marker and no external PackageReference', () => {
    const files = opensdkDotnet(readIR('1.basic'));
    for (const [rel, content] of Object.entries(files)) {
      expect(content, rel).toContain('Code generated by opensdk. DO NOT EDIT.');
    }
    expect(files['Petstore.csproj']).not.toContain('PackageReference');
  });

  it('the client exposes resource services as properties and reads the env credential', () => {
    const client = opensdkDotnet(readIR('1.basic'))['Client.cs'];
    expect(client).toContain('public sealed class PetstoreClient');
    expect(client).toContain('public PetsService Pets { get; }');
    expect(client).toContain('Environment.GetEnvironmentVariable("PETSTORE_API_KEY")');
  });

  it('resource methods are async, page-typed, path-guarded and typed to the model', () => {
    const service = opensdkDotnet(readIR('1.basic'))['PetsService.cs'];
    expect(service).toContain('public async Task<CursorPage<Pet>> List(');
    expect(service).toContain('_transport.RequestAsync<CursorPage<Pet>>');
    expect(service).toContain('public async Task<Pet> Create(CreatePetRequest body');
    expect(service).toContain('public async Task<Pet> Retrieve(string petId');
    expect(service).toContain('public async Task<byte[]> DownloadPhoto(string petId');
    expect(service).toContain('throw new ArgumentException("Expected a non-empty value for petId.", nameof(petId));');
    expect(service).toContain('$"/pets/{petId}/photo"');
  });

  it('models use System.Text.Json wire-name attributes and a string-enum converter', () => {
    const models = opensdkDotnet(readIR('1.basic'))['Models.cs'];
    expect(models).toContain('[JsonPropertyName("has_more")]');
    expect(models).toContain('public bool? HasMore { get; set; }');
    expect(models).toContain('[JsonConverter(typeof(PetStatusConverter))]');
    expect(models).toContain('"available" => PetStatus.Available,');
    expect(models).toContain('PetStatus.Available => "available",');
  });

  it('the runtime interpolates the sdk-behavior policy (retry, timeout, UA, error kinds, request id)', () => {
    const transport = opensdkDotnet(readIR('2.wire'))['Transport.cs'];
    expect(transport).toContain('private const int MaxRetries = 2;');
    expect(transport).toContain('private static readonly HashSet<int> RetryableStatusCodes = new() { 408, 429, 500, 502, 503, 504 };');
    expect(transport).toContain('private const int DefaultTimeoutMs = 60000;');
    expect(transport).toContain('private const string RequestIdHeader = "X-Request-ID";');
    expect(transport).toContain('["CLAUDE_CODE"] = "claude-code",');
    expect(transport).toContain('public sealed class NotFoundException : ApiException');
    expect(transport).toContain('public sealed class RateLimitedException : ApiException');
    expect(transport).toContain('public sealed class InternalException : ApiException');
    // multipart / form encoders + idempotency injection.
    expect(transport).toContain('MultipartFormDataContent');
    expect(transport).toContain('new FormUrlEncodedContent');
  });

  it('wire encodings: multipart body map, form body, header params, exploded/deepObject query, idempotency', () => {
    const files = opensdkDotnet(readIR('2.wire'));
    const service = files['FilesService.cs'];
    // multipart upload builds a field map + routes through the multipart encoder.
    expect(service).toContain('var bodyMap = new Dictionary<string, object?>');
    expect(service).toContain('["file"] = body.File,');
    expect(service).toContain('encoding: "multipart"');
    // header params sent under their wire names.
    expect(service).toContain('["Idempotency-Key"] = idempotencyKey,');
    expect(service).toContain('["X-Request-Priority"] = requestPriority,');
    // exploded array keeps its wire key; explode=false comma-joins; deepObject expands.
    expect(service).toContain('["tags[]"] = tags,');
    expect(service).toContain('["statuses"] = Transport.JoinCsv(statuses),');
    expect(service).toContain('query[$"metadata[{entry.Key}]"] = entry.Value;');
    // the flagged form POST injects a generated idempotency key.
    expect(files['TokensService.cs']).toContain('encoding: "form"');
    expect(files['TokensService.cs']).toContain('idempotency: true');
  });

  it('unions decode by discriminator, const fields auto-fill, and offset/cursor pages are typed', () => {
    const files = opensdkDotnet(readIR('3.unions'));
    const models = files['Models.cs'];
    expect(models).toContain('internal static class ShapeUnion');
    expect(models).toContain('case "circle":');
    expect(models).toContain('return JsonSerializer.Deserialize<Circle>(json, Options);');
    // const body field seeds its fixed literal as a default (request auto-fill).
    expect(models).toContain('public string? Object { get; set; } = "event";');
    const shapes = files['ShapesService.cs'];
    expect(shapes).toContain('public async Task<object?> Retrieve(string shapeId');
    expect(shapes).toContain('return ShapeUnion.Decode(content);');
    expect(files['EventsService.cs']).toContain('public async Task<CursorPage<Event>> List(');
    expect(files['LogsService.cs']).toContain('public async Task<OffsetPage<Log>> List(');
  });

  it('the generated dependency-free test project references the SDK and vendors [Fact]', () => {
    const files = opensdkDotnet(readIR('1.basic'));
    expect(files['Petstore.Tests/Petstore.Tests.csproj']).toContain('<ProjectReference Include="../Petstore.csproj" />');
    expect(files['Petstore.Tests/Petstore.Tests.csproj']).not.toContain('PackageReference');
    expect(files['Petstore.Tests/TestFramework.cs']).toContain('public sealed class FactAttribute : Attribute');
    const petsTests = files['Petstore.Tests/PetsTests.cs'];
    expect(petsTests).toContain('[Fact]');
    expect(petsTests).toContain('await Assert.ThrowsAsync<ArgumentException>(async () => await client.Pets.Retrieve(""));');
  });
});
