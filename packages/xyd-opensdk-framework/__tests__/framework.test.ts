import { describe, expect, it } from 'vitest';

import type { OpensdkSpecJson } from '@xyd-js/opensdk-core';

import type { Emitter } from '../src';
import { applyConfig, generate, generateFileMap, getEmitter, registerEmitter } from '../src';

const spec: OpensdkSpecJson = {
  opensdk: '1.0.0',
  info: { title: 'Demo', version: '1.0.0' },
  types: [{ name: 'Pet', kind: 'struct', fields: [] }],
  resources: [{ name: 'pets', methods: [{ action: 'list', httpMethod: 'get', path: '/pets' }] }],
};

const toy: Emitter = {
  language: 'toy',
  generateProject: () => [{ path: 'project.toy', content: 'project' }],
  generateClient: (_s, ctx) => [{ path: 'client.toy', content: `pkg=${ctx.emitterOptions.packageName ?? 'default'}` }],
  generateTypes: (types) => types.map((t) => ({ path: `types/${t.name}.toy`, content: t.kind })),
  generateResources: (resources) => resources.map((r) => ({ path: `${r.name}.toy`, content: `${r.methods?.length}` })),
  generateRuntime: () => [],
};

describe('orchestrator', () => {
  it('drives every capability method and assembles the file map', () => {
    const files = generate(spec, toy, { packageName: 'demo' });
    expect(Object.keys(files).sort()).toEqual(['client.toy', 'pets.toy', 'project.toy', 'types/Pet.toy']);
    expect(files['client.toy']).toBe('pkg=demo');
    expect(files['types/Pet.toy']).toBe('struct');
  });

  it('throws when two capabilities emit the same path', () => {
    const clash: Emitter = { ...toy, generateRuntime: () => [{ path: 'client.toy', content: 'dup' }] };
    expect(() => generate(spec, clash)).toThrow(/re-emitted client\.toy/);
  });
});

describe('orchestrator: fileHeader', () => {
  const HEADER = 'Code generated by opensdk. DO NOT EDIT.';
  const headed: Emitter = {
    ...toy,
    fileHeader: () => HEADER,
    generateRuntime: () => [
      { path: 'runtime/http.go', content: 'package demo\n' },
      { path: 'runtime/_client.py', content: 'class Client: ...\n' },
      { path: 'package.json', content: '{}\n' },
      { path: 'README.md', content: '# demo\n' },
      { path: 'go.mod', content: 'module demo\n' },
      { path: 'Makefile', content: 'all:\n' },
      { path: 'runtime/already.go', content: `// ${HEADER}\n\npackage demo\n` },
    ],
  };

  it('prepends the header with per-extension comment syntax, once, and skips unknown extensions', () => {
    const files = generate(spec, headed);
    expect(files['runtime/http.go']).toBe(`// ${HEADER}\n\npackage demo\n`);
    expect(files['runtime/_client.py']).toBe(`# ${HEADER}\n\nclass Client: ...\n`);
    // Unknown extensions / no extension: untouched.
    expect(files['package.json']).toBe('{}\n');
    expect(files['README.md']).toBe('# demo\n');
    expect(files['go.mod']).toBe('module demo\n');
    expect(files['Makefile']).toBe('all:\n');
    // Idempotent: an emitter-emitted header is not doubled.
    expect(files['runtime/already.go']).toBe(`// ${HEADER}\n\npackage demo\n`);
    // Unknown emitter extensions (.toy) are skipped too.
    expect(files['project.toy']).toBe('project');
  });

  it('a null header (or no fileHeader at all) leaves content untouched', () => {
    const optOut: Emitter = { ...headed, fileHeader: () => null };
    expect(generate(spec, optOut)['runtime/http.go']).toBe('package demo\n');
    expect(generate(spec, toy)['project.toy']).toBe('project');
  });
});

describe('orchestrator: generateFileMap', () => {
  it('preserves per-file writeMode and applies the header (generate() flattens the same pipeline)', () => {
    const scaffolding: Emitter = {
      ...toy,
      fileHeader: () => 'Code generated by opensdk. DO NOT EDIT.',
      generateProject: () => [
        { path: 'go.mod', content: 'module demo\n', writeMode: 'skipIfExists' },
        { path: 'package.json', content: '{}\n', writeMode: 'mergeJson' },
      ],
    };
    const entries = generateFileMap(spec, scaffolding);
    expect(entries['go.mod']).toEqual({ content: 'module demo\n', writeMode: 'skipIfExists' });
    expect(entries['package.json']).toEqual({ content: '{}\n', writeMode: 'mergeJson' });
    expect(entries['pets.toy'].writeMode).toBeUndefined();
    expect(entries['types/Pet.toy']).toEqual({ content: 'struct' });
    expect(generate(spec, scaffolding)['go.mod']).toBe('module demo\n');
  });
});

describe('registry', () => {
  it('registers via config and resolves by language', () => {
    applyConfig({ emitters: [toy] });
    expect(getEmitter('toy')).toBe(toy);
    expect(() => getEmitter('cobol')).toThrow(/Unknown opensdk language: cobol/);
  });

  it('registerEmitter overrides by language key', () => {
    const other: Emitter = { ...toy };
    registerEmitter(other);
    expect(getEmitter('toy')).toBe(other);
  });
});
