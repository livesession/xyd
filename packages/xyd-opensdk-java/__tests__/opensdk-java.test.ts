import fs from 'node:fs';
import path from 'node:path';

import { describe, expect, it } from 'vitest';

import { opensdkJava } from '../index';
import { JAVA_SMOKE, javacSmoke, testFixture } from './utils';

const fixtures: { name: string; description: string }[] = [
  {
    name: '1.basic',
    description:
      'client + builder, path-qualified services, params builders, typed model POJOs / enum, cursor page container, sdk-behavior runtime, generated tests',
  },
  {
    name: '2.wire',
    description:
      'wire robustness: multipart upload (byte[] file part), urlencoded form body, header params, wireName + explode/deepObject query serialization, sdk-behavior runtime (retry policy, default timeout, UA, error kinds + requestId)',
  },
  {
    name: '3.unions',
    description:
      'type-system consumption: discriminated-union decode (mapping + raw fallback), const auto-fill on request marshal, cursor/offset page containers',
  },
];

describe('opensdk-java', () => {
  for (const f of fixtures) {
    it(f.description, () => {
      testFixture(f.name);
    });
  }

  if (JAVA_SMOKE) {
    for (const f of fixtures) {
      it(`javac -d compiles the generated project: ${f.name}`, () => {
        javacSmoke(f.name);
      });
    }
  }
});

// Shape assertions independent of the golden bytes: the openai-java idioms the
// emitter must keep (client accessor call shape, params builders, path-qualified
// service names, the dependency-free runtime surface).
describe('opensdk-java shape', () => {
  const readIR = (name: string) =>
    JSON.parse(fs.readFileSync(path.join(__dirname, '../__fixtures__', name, 'input.json'), 'utf8'));

  it('every generated .java file carries the ownership header', () => {
    const files = opensdkJava(readIR('1.basic'));
    for (const [rel, content] of Object.entries(files)) {
      if (!rel.endsWith('.java')) continue;
      expect(content.startsWith('// Code generated by opensdk. DO NOT EDIT.\n\n'), rel).toBe(true);
    }
  });

  it('emits the maven manifest and a Maven-layout source tree', () => {
    const files = opensdkJava(readIR('1.basic'));
    expect(files['pom.xml']).toContain('<artifactId>petstore-java</artifactId>');
    expect(files['src/main/java/com/example/petstore/Client.java']).toBeDefined();
    // Path-qualified, singularized service class name (openai-java shape).
    expect(files['src/main/java/com/example/petstore/PetService.java']).toBeDefined();
  });

  it('client exposes a resource accessor + builder + fromEnv seeded from the auth env var', () => {
    const client = opensdkJava(readIR('1.basic'))['src/main/java/com/example/petstore/Client.java'];
    expect(client).toContain('public PetService pets() {');
    expect(client).toContain('public static Builder builder() {');
    expect(client).toContain('System.getenv("PETSTORE_API_KEY")');
    expect(client).toContain('private String baseUrl = "https://api.petstore.io/v1";');
  });

  it('service methods mirror the openai-java call shape (create/list/retrieve/downloadPhoto)', () => {
    const service = opensdkJava(readIR('1.basic'))['src/main/java/com/example/petstore/PetService.java'];
    // A cursor-paginated list returns the generic page container decoding {data, has_more}.
    expect(service).toContain('public CursorPage<Pet> list(PetListParams params) {');
    expect(service).toContain('return CursorPage.fromJson(transport.execute(request), __e -> Pet.fromJson(__e));');
    expect(service).toContain('public Pet create(PetCreateParams params) {');
    expect(service).toContain('public Pet retrieve(String petId) {');
    // Binary download returns raw bytes via the raw transport path.
    expect(service).toContain('public byte[] downloadPhoto(String petId) {');
    expect(service).toContain('return transport.executeRaw(request);');
    // Required string path param is guarded before the path is built.
    expect(service).toContain('missing required pet_id parameter');
  });

  it('phase-2: page container, typed error kinds, sdk-behavior runtime, generated tests', () => {
    const files = opensdkJava(readIR('1.basic'));
    // Cursor page container is vendored (data + has_more decode).
    expect(files['src/main/java/com/example/petstore/CursorPage.java']).toContain('public final class CursorPage<T> {');
    // Typed error kinds from sdkBehavior + request-id capture on the base.
    expect(files['src/main/java/com/example/petstore/NotFoundException.java']).toContain(
      'public final class NotFoundException extends ApiException {',
    );
    const api = files['src/main/java/com/example/petstore/ApiException.java'];
    expect(api).toContain('public static ApiException of(int statusCode, String message, byte[] rawBody, String requestId) {');
    expect(api).toContain('case 404:');
    // Retry/timeout/UA interpolated from sdkBehavior defaults.
    const transport = files['src/main/java/com/example/petstore/Transport.java'];
    expect(transport).toContain('private static final int MAX_RETRIES = 2;');
    expect(transport).toContain('private static final long DEFAULT_TIMEOUT_MS = 60000L;');
    expect(transport).toContain('java.util.Set.of(408, 429, 500, 502, 503, 504)');
    expect(transport).toContain('{"CLAUDE_CODE", "claude-code"},');
    // The SDK's own dependency-free assertion test (main + path-param guard).
    const test = files['src/main/java/com/example/petstore/PetServiceTest.java'];
    expect(test).toContain('public static void main(String[] args) {');
    expect(test).toContain('requireContains(expected.getMessage(), "missing required");');
  });

  it('params classes are builders; models are typed POJOs with a manual codec', () => {
    const files = opensdkJava(readIR('1.basic'));
    const createParams = files['src/main/java/com/example/petstore/PetCreateParams.java'];
    expect(createParams).toContain('public static Builder builder() {');
    expect(createParams).toContain('public String name() {'); // required accessor
    expect(createParams).toContain('public Optional<PetStatus> status() {'); // optional accessor

    const pet = files['src/main/java/com/example/petstore/Pet.java'];
    expect(pet).toContain('public final class Pet implements Json.JsonSerializable {');
    expect(pet).toContain('public static Pet fromJson(Object json) {');
    expect(pet).toContain('public Map<String, Object> toJsonMap() {');

    const status = files['src/main/java/com/example/petstore/PetStatus.java'];
    expect(status).toContain('public enum PetStatus implements Json.JsonEnum {');
    expect(status).toContain('AVAILABLE("available")');
  });

  it('ships a dependency-free stdlib runtime (java.net.http + hand-rolled Json, no okhttp/jackson)', () => {
    const files = opensdkJava(readIR('1.basic'));
    const transport = files['src/main/java/com/example/petstore/Transport.java'];
    expect(transport).toContain('import java.net.http.HttpClient;');
    expect(transport).not.toMatch(/okhttp|jackson|com\.fasterxml/i);
    expect(transport).toContain('builder.header("Authorization", "Bearer " + apiKey);');
    expect(files['src/main/java/com/example/petstore/Json.java']).toContain('public static Object parse(String text) {');
    expect(files['pom.xml']).not.toMatch(/<dependencies>/);
  });
});
