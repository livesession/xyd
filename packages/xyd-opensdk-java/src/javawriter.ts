// A tiny string-emitting Java writer (no Java toolchain): the ownership header,
// import blocks and a Javadoc helper. Mirrors gowriter/PyUses in the sibling
// emitters — pure text assembly.

/** The machine-ownership marker prepended (as `// ...`) to every generated .java file. */
export const HEADER = 'Code generated by opensdk. DO NOT EDIT.';

/**
 * Assemble a complete `.java` file: ownership header + package clause +
 * (deduped, sorted) imports + body. `.java` is not in the framework
 * orchestrator's comment-syntax map, so the header is applied HERE.
 */
export function javaFile(pkg: string, imports: string[], body: string): string {
  const parts = [`// ${HEADER}`, '', `package ${pkg};`];
  const uniqueSorted = [...new Set(imports)].sort();
  if (uniqueSorted.length > 0) {
    parts.push('', uniqueSorted.map((i) => `import ${i};`).join('\n'));
  }
  parts.push('', body.trimEnd());
  return `${parts.join('\n')}\n`;
}

/** A Javadoc block (`/** ... *\/`) from free text, or '' when empty. */
export function javaDoc(text: string | undefined, indent = ''): string {
  const raw = (text || '').trim();
  if (!raw) return '';
  const lines = raw.split('\n');
  if (lines.length === 1) return `${indent}/** ${lines[0].trim()} */`;
  const body = lines.map((l) => `${indent} * ${l.trim()}`).join('\n');
  return `${indent}/**\n${body}\n${indent} */`;
}
