// Post-processes the flat sqlc-gen-typescript output (`dbnode/<resource>_sql.ts`,
// which combines row/arg types + query functions in one file) into a fastd-style
// per-resource package: `dbnode/<resource>/{models.gen.ts, queries.gen.ts, index.ts}`
// (analogue of fastd `dbgo/<resource>/{models.go, queries.sql.go}`). Runs after
// `sqlc generate` (see the `gen:sql` script). Idempotent: re-run overwrites.
import {
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const dbnode = join(
  fileURLToPath(new URL(".", import.meta.url)),
  "../../dbnode",
);

const HEADER = "// Code generated by sqlc + split-dbnode.ts. DO NOT EDIT.\n\n";

/** Split a source file into top-level blocks, respecting `{}` nesting and
 * backtick template literals (SQL strings) so blank lines inside a query or a
 * type body never split a block. */
function toBlocks(src: string): string[] {
  const lines = src.split("\n");
  const blocks: string[] = [];
  let cur: string[] = [];
  let depth = 0;
  let inTick = false;
  for (const line of lines) {
    if (line.trim() === "" && depth === 0 && !inTick) {
      if (cur.length) {
        blocks.push(cur.join("\n"));
        cur = [];
      }
      continue;
    }
    cur.push(line);
    for (const ch of line) {
      if (ch === "`") inTick = !inTick;
      else if (!inTick) {
        if (ch === "{") depth++;
        else if (ch === "}") depth--;
      }
    }
  }
  if (cur.length) blocks.push(cur.join("\n"));
  return blocks;
}

const files = readdirSync(dbnode).filter((f) => f.endsWith("_sql.ts"));
if (!files.length) {
  console.log("[split-dbnode] no *_sql.ts files (already split?)");
}

for (const file of files) {
  const resource = file.replace(/_sql\.ts$/, "");
  const src = readFileSync(join(dbnode, file), "utf8");
  const blocks = toBlocks(src);

  const modelBlocks: string[] = [];
  const queryBlocks: string[] = [];
  let clientBlock = "";
  const modelNames: string[] = [];

  for (const b of blocks) {
    const t = b.trimStart();
    if (t.startsWith("import ") && b.includes('from "pg"')) {
      // dropped — queries.gen re-emits its own pg import
    } else if (t.startsWith("interface Client")) {
      clientBlock = b;
    } else if (t.startsWith("export interface")) {
      const m = t.match(/export interface (\w+)/);
      if (m) modelNames.push(m[1]);
      modelBlocks.push(b);
    } else {
      // export const <x>Query = `...`  /  export async function <x>(...)
      queryBlocks.push(b);
    }
  }

  const dir = join(dbnode, resource);
  mkdirSync(dir, { recursive: true });

  // models.gen.ts — the row/arg interfaces (data shapes).
  writeFileSync(
    join(dir, "models.gen.ts"),
    HEADER + (modelBlocks.join("\n\n") || "export {};") + "\n",
  );

  // queries.gen.ts — pg import + Client + the SQL consts + functions, typed via
  // the split-out models.
  const typeImport = modelNames.length
    ? `import type {\n${modelNames.map((n) => `  ${n},`).join("\n")}\n} from "./models.gen";\n`
    : "";
  const queriesOut = [
    HEADER.trimEnd(),
    "",
    'import { QueryArrayConfig, QueryArrayResult } from "pg";',
    typeImport.trimEnd(),
    "",
    clientBlock,
    "",
    queryBlocks.join("\n\n"),
    "",
  ]
    .filter((s) => s !== "")
    .join("\n");
  writeFileSync(join(dir, "queries.gen.ts"), queriesOut + "\n");

  // index.ts — the package barrel (import the folder, get models + queries).
  writeFileSync(
    join(dir, "index.ts"),
    HEADER + 'export * from "./models.gen";\nexport * from "./queries.gen";\n',
  );

  rmSync(join(dbnode, file));
  console.log(`[split-dbnode] ${file} → ${resource}/{models,queries}.gen.ts`);
}
